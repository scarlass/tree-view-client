export namespace HierarchyUtil {
    const boolType = ["True", "False"] as const;

    export function toHierarchy<T extends ObjectType>(
        data: T[],
        ...groupFns: (GroupFn<T> | Keyof<T>)[]
    ): ModelEntry<T> {
        const root: ModelEntry<T> = {
            name: "",
            get data() {
                return data;
            },
            children: new Map(),
        };
        groupFns = groupFns
            .map((e) => {
                if (isStr(e)) return (d: T) => d[e];
                else if (!isFn(e)) return null;
                return e as GroupFn<T>;
            })
            .filter(isDefined) as GroupFn<T>[];

        const [fn1, ...fns] = groupFns as GroupFn<T>[];
        const seen: Set<any> = new Set(data.map(fn1));

        for (const s of seen) {
            if (root.name === "") {
                const d = data[0];
                const keys = Object.keys(d);
                const idx = keys.findIndex((k) => d[k] === s);
                if (idx !== -1) root.name = keys[idx];
            }

            if (groupFns.length >= 1) {
                const filter = data.filter((e) => fn1(e) === s);
                if (fns.length >= 1)
                    root.children.set(s, toHierarchy(filter, ...fns));
                else root.children.set(s, null);
            }
        }
        return root;
    }

    export function toRD3Hierarchy<T extends ObjectType>(
        data: T[],
        dataKeys: Keyof<T>[]
    ) {
        if(dataKeys.length === 0) {
            // console.warn("No value were filtered, returning empty value!")
            return [];
        }

        const root: RModelEntry<T>[] = [];
        const [key, ...restKey] = dataKeys;
        const [fn, ...restFn] = dataKeys.map<GroupFn<T>>(e => {
            return (d) => d[e];
        });
        const seen = new Set(data.map(fn));

        // console.log(seen);
        for(const s of seen) {
            const entry: RModelEntry<T> = {
                name: key,
                attributes: {
                    value: s
                },
                children: [],
                get data() { return data.filter(e => e[key] === s); }
            }

            if(dataKeys.length >= 1) {
                entry.children.push(...toRD3Hierarchy(entry.data, restKey))
            }

            root.push(entry);
        }


        return root;
    }

    export function filterHierarchyValue<T extends ObjectType>(
        me: ModelEntry<T>,
        filter: EntryData<QueryEnumData>[]
    ) {
        const [ed1, ...edRest] = filter;
        const nme: ModelEntry<T> = {
            name: me.name,
            data: me.data,
            children: new Map(me.children),
        };

        if (filter.length === 0) return nme;

        const dataObj = ed1.data;
        for (const entry of Object.entries(dataObj.value)) {
            const key = entry[0];
            // const alias = isArr(entry[1]) ? entry[1][0] : entry[0];
            const value = isArr(entry[1]) ? entry[1][1] : entry[1];

            if (!value) {
                const c = nme.children;
                if (nme.children.has(key)) {
                    nme.children.delete(key);
                }
                // else if (nh.children.has(alias)) nh.children.delete(alias);
                else if (boolType.includes(key as any)) {
                    if (key === "True" && c.has(true)) {
                        c.delete(true);
                    } else {
                        c.delete(false);
                    }
                } else if (key === "NONE") {
                    const c = nme.children;
                    if (c.has("")) {
                        nme.children.delete("");
                    } else if (c.has(0)) {
                        nme.children.delete(0);
                    }
                }

                nme.data = [...nme.data].filter((data) =>
                    filterHierarchyData(data, ed1.data.column as any, key)
                );
            }
        }

        if (edRest.length !== 0) {
            nme.children = new Map(
                [...nme.children].map((e) => {
                    if (e[1] !== null)
                        return [e[0], filterHierarchyValue(e[1], edRest)];
                    return [e[0], null];
                })
            );
        }

        return nme;
    }

    export function filterRD3HierarchyValue<T extends ObjectType>(
        rmd: RModelEntry<T>,
        filter: EntryData<QueryEnumData>[]
    ) {
        const [ed1, ...edRest] = filter;
        const nrmd: RModelEntry<T> = {
            name: rmd.name,
            attributes: rmd.attributes,
            children: [...rmd.children],
            data: rmd.data
        }
        if (filter.length === 0) return;

        const dataObj = ed1.data;
        const find = Object.entries(dataObj.value).findIndex(entry => {
            const key = entry[0];
            const attrVal = nrmd.attributes?.value;
            const value = isArr(entry[1]) ? entry[1][1] : entry[1];

            if(!value) {
                if(attrVal === key) return true;
                else if(boolType.includes(key as any)) {
                    if(key === boolType[0] && attrVal === true) return true;
                    else if(key === boolType[1] && attrVal === false) return true;
                }
                else if(key === "NONE") {
                    if(attrVal === "") return true;
                }
            }
            return false;
        });
        if(find !== -1) return;

        if(edRest.length >= 1) {
            nrmd.children = nrmd.children.map(e => filterRD3HierarchyValue(e, edRest)).filter(isDefined);
        }

        return nrmd;
    }

    function filterHierarchyData<T extends ObjectType>(
        data: T,
        column: keyof T,
        predictKey: string
    ) {
        const colKey = column;
        const colValue = data[colKey];

        if (colValue === predictKey) return false;
        else if (boolType.includes(predictKey as any)) {
            // console.log(colKey, colValue);
            if (boolType[0] === predictKey && colValue === true) return false;
            else if (boolType[1] === predictKey && colValue === false)
                return false;
        } else if (predictKey === "NONE") {
            if (colValue === "") return false;
            else if (colValue === 0) return false;
        }
        return true;
    }
}
